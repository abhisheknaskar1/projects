                        <h2 class="font-weight-bold mb-2">Who Can Apply for a Money Transfer License in Nigeria?</h2>
                        <h3><strong>Eligibility Criteria </strong></h3>
                        <p>To obtain an IMTO license, the applicant must be a registered company that meets the CBN’s financial, legal, and operational requirements.</p>

                        <h3><strong>Who is NOT Eligible? </strong></h3>
                        <ul class="mb-2 blog-sec-layout">
                            <li><strong>Banks</strong> (They may act as agents but cannot operate their own IMTO business).</li>
                            <li><strong>Fintech companies </strong> (Previously allowed, now strictly prohibited).</li>
                            <li><strong>Unregistered entities or individuals</strong></li>
                        </ul>
                        <h3><strong>Requirements for Obtaining an IMTO License</strong></h3>
                        <p>To <strong>apply for an IMTO license</strong>, a company must submit an application to the <strong>Director, Trade & Exchange Department of the CBN</strong>, along with the following documents:</p>

                        <p><strong>Required Documents</strong></p>
                        <ol><strong>
                                <li>1. Proof of approval to operate internationally (For foreign IMTOs)</li>
                                <li>2. Certificate of incorporation & tax clearance (For Nigerian companies)</li>
                                <li>3. Ownership structure & board approval</li>
                                <li>4. Company profile, including CVs of key executives</li>
                                <li>5. Credit reports of shareholders & management </li>
                                <li>6. Details of technical partners (if applicable)</li>
                            </strong></ol>
                        <p>Upon successful review, the <strong>CBN grants an Approval-in-Principle (AIP)</strong>, allowing the applicant to set up its operations. However, the final license is only granted once all compliance conditions are met.</p>

                        <h3><strong>Annual Renewal Requirement</strong></h3>
                        <ul class="mb-2 blog-sec-layout">
                            <li>IMTO licenses must be <strong>renewed annually by January 31st</strong></li>
                            <li>The renewal fee is ₦10,000,000.</li>
                            <li>Failure to renew on time may result in suspension or revocation.</li>
                        </ul>
