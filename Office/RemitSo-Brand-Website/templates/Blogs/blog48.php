                        <h2 class="font-weight-bold mb-2">Your IMTO Licensing Questions, Answered</h2>
                        <h3><strong>1. What is an IMTO in Nigeria?</strong></h3>
                        <p>An International Money Transfer Operator (IMTO) is a CBN-licensed company that facilitates inbound remittances from individuals and businesses abroad to recipients in Nigeria.</p>

                        <h3><strong>2. Can fintech companies apply for an IMTO license in Nigeria?</strong></h3>
                        <p>No, the 2024 CBN guidelines prohibit fintech companies from obtaining an IMTO license. However, they can partner with licensed IMTOs to offer money transfer services.</p>

                        <h3><strong>3. What is the cost of an IMTO license in Nigeria?</strong></h3>
                        <ul class="mb-2 blog-sec-layout">
                            <li>The initial license fee is ₦10,000,000.</li>
                            <li>The annual renewal fee is also ₦10,000,000, due before January 31st each year.</li>
                        </ul>
                        <h3><strong>4. Can an IMTO operate outbound transfers from Nigeria?</strong></h3>
                        <p>No, the updated guidelines restrict IMTOs to inbound transactions only. Outbound transfers must be handled through other financial institutions.</p>

                        <h3><strong>5. What are the permitted money transfer methods in Nigeria?</strong></h3>
                        <p>IMTOs can facilitate transfers via:</p>
                        <ul class="mb-2 blog-sec-layout">
                            <li><strong>Bank transfers</strong></li>
                            <li><strong>Wire transfers</strong></li>
                            <li><strong>In-person cash pickups</strong></li>
                            <li><strong>Mobile money & digital wallets</strong></li>
                        </ul>
                        <h3><strong>6. How long does it take to get an IMTO license in Nigeria?</strong></h3>
                        <p>The licensing process includes:</p>
                        <ul class="mb-2 blog-sec-layout">
                            <li>1. Application submission</li>
                            <li>2. Approval-in-Principle (AIP) issuance</li>
                            <li>3. Final approval within 3 months (if all conditions are met).</li>
                        </ul>
                        <h3><strong>7. Can banks operate as IMTOs in Nigeria?</strong></h3>
                        <p>No, banks cannot obtain an IMTO license, but they can act as agents for licensed IMTOs.</p>

                        <h3><strong>8. How are beneficiaries paid in Nigeria?</strong></h3>
                        <p>Beneficiaries receive payments in Naira via:</p>
                        <ul class="mb-2 blog-sec-layout">
                            <li>Bank deposits (mandatory for amounts exceeding $200)</li>
                            <li>Cash pickup at authorized locations</li>
                        </ul>

                        <h3><strong>9. Are IMTOs allowed to buy foreign exchange in Nigeria?</strong></h3>
                        <p>No, IMTOs cannot purchase forex from the domestic foreign exchange market for settlement purposes.</p>

                        <h3><strong>10. How can I start a money transfer business in Nigeria?</strong></h3>
                        <p>To start a money transfer business in Nigeria, you must:</p>
                        <ul class="mb-2 blog-sec-layout">
                            <li>Register a company with the Corporate Affairs Commission (CAC)</li>
                            <li>Meet the capital requirement ($1 million or naira equivalent)</li>
                            <li>Apply for an IMTO license from the CBN</li>
                            <li>Comply with AML, KYC, and other financial regulations</li>
                        </ul>

