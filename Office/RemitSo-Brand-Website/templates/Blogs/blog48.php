                        <p>The <strong>Central Bank of Nigeria (CBN)</strong> recently introduced a revised regulatory framework for International Money Transfer Operators (IMTOs) in the country. Released on January 31, 2024, these guidelines outline the requirements, operational scope, ownership structures, compliance obligations, and governance standards for companies engaged in international money transfer in Nigeria.</p>
                        <p>This regulatory update reflects the CBN’s commitment to boosting forex liquidity, increasing diaspora remittances, and improving the ease of doing business for IMTOs. The new framework also brings stricter licensing conditions, clearer compliance requirements, and enhanced customer protections.</p>
