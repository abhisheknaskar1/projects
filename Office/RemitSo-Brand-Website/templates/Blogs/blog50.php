                            <li><strong>International Transfers:</strong> Sending money across borders, e.g., transferring money from India to the USA.</li>
                            <li><strong>Business Transfers:</strong> Facilitating transactions between businesses, including transferring money between bank accounts.</li>
                        </ul>

